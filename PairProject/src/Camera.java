
public class Camera {
	
	private final int cameraWidth = 0;
	private final int cameraHeight = 0;
	private final int dimX = 0;
	private final int dimY = 0;
	
	public Camera () {
		
	}
	
	public int getCameraWidth() {
		return cameraWidth;
	}
	
	public int getCameraHeight() {
		return cameraHeight;
	}
	
	public int dimX() {
		return dimX;
	}
	
	public int dimY() {
		return dimY;
	}
	
	
}
